import Ajv2020 from 'ajv/dist/2020.js';
import { mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const dashboardRoot = join(scriptDir, '..');
const repoRoot = join(dashboardRoot, '..');
const schemaDir = join(repoRoot, 'docs', 'contracts', 'dashboard-api');
const outputPath = join(dashboardRoot, 'src', 'api', 'generated', 'dashboardApiContracts.ts');
const checkOnly = process.argv.includes('--check');

const ajv = new Ajv2020({ strict: false });
const schemas = readdirSync(schemaDir)
  .filter((name) => name.endsWith('.schema.json'))
  .sort()
  .map((name) => {
    const path = join(schemaDir, name);
    const schema = JSON.parse(readFileSync(path, 'utf8'));
    if (!ajv.validateSchema(schema)) {
      throw new Error(`${name} is not a valid JSON Schema: ${ajv.errorsText(ajv.errors)}`);
    }
    return { name, schema };
  });

function exportName(name) {
  return name
    .replace(/\.schema\.json$/, '')
    .replace(/[^a-zA-Z0-9]+(.)/g, (_, chr) => chr.toUpperCase())
    .replace(/^./, (chr) => chr.toLowerCase());
}

const lines = [
  '// Generated by dashboard/scripts/generate-api-contracts.mjs.',
  '// Source: docs/contracts/dashboard-api/*.schema.json',
  '',
  'export interface DashboardApiContractSchema {',
  "  readonly $schema: string;",
  "  readonly $id: string;",
  '  readonly title: string;',
  '  readonly [key: string]: unknown;',
  '}',
  '',
];

for (const { name, schema } of schemas) {
  lines.push(`export const ${exportName(name)} = ${JSON.stringify(schema, null, 2)} as const satisfies DashboardApiContractSchema;`);
  lines.push('');
}

lines.push('export const dashboardApiContractSchemas = {');
for (const { name } of schemas) {
  lines.push(`  ${exportName(name)},`);
}
lines.push('} as const;');
lines.push('');
lines.push('export const dashboardApiContractSchemaIds = Object.values(dashboardApiContractSchemas).map(');
lines.push('  (schema) => schema.$id,');
lines.push(');');
lines.push('');

const generated = `${lines.join('\n')}`;
if (checkOnly) {
  const current = readFileSync(outputPath, 'utf8');
  if (current !== generated) {
    throw new Error(`${relative(repoRoot, outputPath)} is out of date. Run npm run contracts:generate from dashboard/.`);
  }
} else {
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, generated);
}
